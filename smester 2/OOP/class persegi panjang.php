<?php
class persegi_panjang {
    private ($panjang);
    private ($lebar);
    function__construct ($p)
    {
        $thin->panjang = $p;
    }
    function__construct ($l)
    {
        $thin->lebar = $l;
    }
    function getluas ()
    {
        return $thin->panjang * $thin->lebar;
    }
    function getkeliling ()
    {
        return 2 * ($thin->panjang + $thin->lebar);
    }
}
